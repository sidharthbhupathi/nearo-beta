export function t(key: string, currentLang: string): string {
  if (!currentLang || currentLang === "English") return key;
  const translationSet = translations[key];
  if (translationSet && translationSet[currentLang]) {
    return translationSet[currentLang];
  }
  
  // Try to find if a partial key matches
  for (const dictKey in translations) {
    if (key.startsWith(dictKey)) {
      const rest = key.substring(dictKey.length);
      const translatedBase = translations[dictKey][currentLang];
      if (translatedBase) return translatedBase + rest;
    }
  }

  return key;
}
